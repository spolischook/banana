<?php

namespace App\Tests\Stub;

use Symfony\Component\Console\Output\Output;

class OutputStub extends Output
{
    public $output = '';

    public function clear()
    {
        $this->output = '';
    }

    protected function doWrite($message, $newline)
    {
        $this->output .= $message.($newline ? "\n" : '');
    }
}
